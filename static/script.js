document.addEventListener('DOMContentLoaded', () => {
    const input = document.getElementById('newsInput');
    const charCount = document.getElementById('charCount');
    const analyzeBtn = document.getElementById('analyzeBtn');
    const clearBtn = document.getElementById('clearBtn');
    const btnText = document.querySelector('.btn-text');
    const btnLoader = document.getElementById('btnLoader');
    
    const resultContainer = document.getElementById('resultContainer');
    const resultIcon = document.getElementById('resultIcon');
    const resultTitle = document.getElementById('resultTitle');
    const confidencePct = document.getElementById('confidencePct');
    const confidenceFill = document.getElementById('confidenceFill');
    const resultMessage = document.getElementById('resultMessage');
    const copyResultBtn = document.getElementById('copyResult');

    const historyList = document.getElementById('historyList');
    const clearHistoryBtn = document.getElementById('clearHistory');
    const exampleChips = document.querySelectorAll('.example-chip');

    let history = JSON.parse(localStorage.getItem('newsHistory') || '[]');

    // Initialize
    renderHistory();

    // Update character count
    input.addEventListener('input', () => {
        const length = input.value.length;
        charCount.textContent = `${length} character${length !== 1 ? 's' : ''}`;
    });

    // Clear Input
    clearBtn.addEventListener('click', () => {
        input.value = '';
        charCount.textContent = '0 characters';
        resultContainer.classList.add('hidden');
    });

    // Example Chips
    exampleChips.forEach(chip => {
        chip.addEventListener('click', () => {
            input.value = chip.dataset.text;
            input.dispatchEvent(new Event('input'));
            input.focus();
        });
    });

    // Analyze Click
    analyzeBtn.addEventListener('click', async () => {
        const text = input.value.trim();
        if (!text) {
            input.parentElement.animate([
                { transform: 'translateX(0)' },
                { transform: 'translateX(-10px)' },
                { transform: 'translateX(10px)' },
                { transform: 'translateX(-10px)' },
                { transform: 'translateX(10px)' },
                { transform: 'translateX(0)' }
            ], { duration: 500 });
            return;
        }

        setLoading(true);

        try {
            const response = await fetch('/predict', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({ text })
            });

            if (!response.ok) throw new Error('Model service unavailable');

            const data = await response.json();
            showResult(data, text);
            addToHistory(data, text);

        } catch (error) {
            console.error('Error:', error);
            alert('Analysis failed: ' + (error.message || 'Check your server connection.'));
        } finally {
            setLoading(false);
        }
    });

    // Copy Result
    copyResultBtn.addEventListener('click', () => {
        const textToCopy = `Analysis Result: ${resultTitle.textContent}\nConfidence: ${confidencePct.textContent}\n\nArticle excerpt: ${input.value}\n\nGenerated by AI Fake News Detector`;
        navigator.clipboard.writeText(textToCopy).then(() => {
            const originalText = copyResultBtn.textContent;
            copyResultBtn.textContent = '✅';
            setTimeout(() => copyResultBtn.textContent = originalText, 2000);
        });
    });

    // History Logic
    function addToHistory(data, text) {
        const item = {
            id: Date.now(),
            text: text,
            label: data.raw_prediction === 1 ? 'REAL' : 'FAKE',
            confidence: (data.confidence * 100).toFixed(1),
            timestamp: new Date().toLocaleTimeString()
        };
        history.unshift(item);
        if (history.length > 20) history.pop(); // Keep last 20
        localStorage.setItem('newsHistory', JSON.stringify(history));
        renderHistory();
    }

    function renderHistory() {
        if (history.length === 0) {
            historyList.innerHTML = '<p class="empty-msg">No recent activity</p>';
            return;
        }

        historyList.innerHTML = history.map(item => `
            <div class="history-item" onclick="loadHistoryItem(${item.id})">
                <p class="h-text">${item.text}</p>
                <div class="h-meta">
                    <span class="${item.label === 'REAL' ? 'h-real' : 'h-fake'}">${item.label}</span>
                    <span class="h-time">${item.confidence}% • ${item.timestamp}</span>
                </div>
            </div>
        `).join('');
    }

    window.loadHistoryItem = (id) => {
        const item = history.find(h => h.id === id);
        if (item) {
            input.value = item.text;
            input.dispatchEvent(new Event('input'));
            analyzeBtn.click();
        }
    };

    clearHistoryBtn.addEventListener('click', () => {
        if (confirm('Clear all recent analysis history?')) {
            history = [];
            localStorage.setItem('newsHistory', JSON.stringify(history));
            renderHistory();
        }
    });

    function setLoading(isLoading) {
        analyzeBtn.disabled = isLoading;
        btnText.style.display = isLoading ? 'none' : 'block';
        btnLoader.style.display = isLoading ? 'block' : 'none';
        if (isLoading) resultContainer.classList.add('hidden');
    }

    function showResult(data, text) {
        resultContainer.classList.remove('hidden', 'result-real', 'result-fake');
        const isReal = data.raw_prediction === 1;
        const confidenceValue = (data.confidence * 100).toFixed(1);

        if (isReal) {
            resultContainer.classList.add('result-real');
            resultIcon.textContent = '✅';
            resultTitle.textContent = 'REAL News';
            resultMessage.textContent = 'This article aligns with factual reporting patterns found in our training data.';
        } else {
            resultContainer.classList.add('result-fake');
            resultIcon.textContent = '❌';
            resultTitle.textContent = 'FAKE News';
            resultMessage.textContent = 'This article exhibits linguistic markers typically associated with misinformation or clickbait.';
        }

        confidencePct.textContent = `${confidenceValue}%`;
        void resultContainer.offsetWidth; // Trigger reflow
        setTimeout(() => {
            confidenceFill.style.width = `${confidenceValue}%`;
        }, 100);
    }
});
